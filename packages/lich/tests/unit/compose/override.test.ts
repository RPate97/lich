import { afterEach, beforeEach, describe, expect, it } from "vitest";
import { mkdtempSync, readFileSync, rmSync } from "node:fs";
import { tmpdir } from "node:os";
import { join } from "node:path";
import { parse as yamlParse } from "yaml";
import type { LichConfig, PortDescriptor } from "../../../src/config/types.js";
import {
  generateComposeOverride,
  writeComposeOverride,
  type OverrideInput,
} from "../../../src/compose/override.js";
import { stackDir } from "../../../src/state/directory.js";

// ---------------------------------------------------------------------------
// Fixtures
// ---------------------------------------------------------------------------

/**
 * Minimal LichConfig builder. Tests pass just the `services` shape they
 * care about — `version` is required for the type, everything else is
 * optional.
 *
 * The parameter is typed as `Record<string, unknown>` (then cast) so tests
 * can pass the dogfood-style port shape (`{ container, env }`) that the
 * override generator reads structurally. The `PortDescriptor` type in
 * `config/types.ts` doesn't yet include `container` on the object form —
 * tightening that type is owned by LEV-305 — so the test fixture takes
 * the same permissive shape the runtime accepts.
 */
function makeConfig(
  services: Record<string, unknown>,
): LichConfig {
  return {
    version: "1",
    services: services as NonNullable<LichConfig["services"]>,
  };
}

/** A single-service config: one compose service `api` with one port. */
const SINGLE_SERVICE_CONFIG = makeConfig({
  api: {
    image: "node:20",
    ports: {
      http: { container: 3000, env: "PORT" },
    },
  },
});

/** Input matching `SINGLE_SERVICE_CONFIG` with one allocated port + env. */
const SINGLE_SERVICE_INPUT: OverrideInput = {
  config: SINGLE_SERVICE_CONFIG,
  allocatedPorts: {
    compose: {
      api: { http: 5847 },
    },
  },
  resolvedEnv: {
    api: {
      DATABASE_URL: "postgresql://user:pass@localhost:5432/db",
      LICH_STACK_ID: "dogfood-stack-a1b2c3d4",
    },
  },
  stackId: "dogfood-stack-a1b2c3d4",
};

// ---------------------------------------------------------------------------
// generateComposeOverride — pure function
// ---------------------------------------------------------------------------

describe("generateComposeOverride", () => {
  it("emits header comment with the stack_id", () => {
    const out = generateComposeOverride(SINGLE_SERVICE_INPUT);
    expect(out.startsWith("# Auto-generated by lich. Do not edit.\n")).toBe(
      true,
    );
    expect(out).toContain(`# stack_id: ${SINGLE_SERVICE_INPUT.stackId}\n`);
  });

  it("does NOT emit a top-level `version:` field", () => {
    const out = generateComposeOverride(SINGLE_SERVICE_INPUT);
    const parsed = yamlParse(out);
    expect(parsed.version).toBeUndefined();
  });

  it("does NOT emit a top-level `name:` field", () => {
    const out = generateComposeOverride(SINGLE_SERVICE_INPUT);
    const parsed = yamlParse(out);
    expect(parsed.name).toBeUndefined();
  });

  it("produces expected YAML for a single service with ports + env", () => {
    const out = generateComposeOverride(SINGLE_SERVICE_INPUT);
    const parsed = yamlParse(out);

    expect(parsed.services).toBeDefined();
    expect(parsed.services.api).toEqual({
      environment: {
        DATABASE_URL: "postgresql://user:pass@localhost:5432/db",
        LICH_STACK_ID: "dogfood-stack-a1b2c3d4",
      },
      ports: ["5847:3000"],
    });
  });

  it("gives each of multiple services its own block", () => {
    const input: OverrideInput = {
      config: makeConfig({
        api: { ports: { http: { container: 3000, env: "PORT" } } },
        web: { ports: { http: { container: 3001, env: "PORT" } } },
      }),
      allocatedPorts: {
        compose: { api: { http: 5000 }, web: { http: 5001 } },
      },
      resolvedEnv: {
        api: { NODE_ENV: "dev" },
        web: { NODE_ENV: "dev" },
      },
      stackId: "stack-multi",
    };
    const parsed = yamlParse(generateComposeOverride(input));
    expect(Object.keys(parsed.services).sort()).toEqual(["api", "web"]);
    expect(parsed.services.api.ports).toEqual(["5000:3000"]);
    expect(parsed.services.web.ports).toEqual(["5001:3001"]);
  });

  it("omits a service that has neither ports nor env", () => {
    const input: OverrideInput = {
      config: makeConfig({
        api: { ports: { http: { container: 3000, env: "PORT" } } },
        worker: { image: "node:20" },
      }),
      allocatedPorts: { compose: { api: { http: 5000 } } },
      resolvedEnv: { api: { NODE_ENV: "dev" } },
      stackId: "stack-omit",
    };
    const parsed = yamlParse(generateComposeOverride(input));
    expect(parsed.services.api).toBeDefined();
    expect(parsed.services.worker).toBeUndefined();
  });

  it("emits only `ports:` when a service has ports but no env", () => {
    const input: OverrideInput = {
      config: makeConfig({
        api: { ports: { http: { container: 3000, env: "PORT" } } },
      }),
      allocatedPorts: { compose: { api: { http: 5000 } } },
      resolvedEnv: { api: {} },
      stackId: "stack-ports-only",
    };
    const parsed = yamlParse(generateComposeOverride(input));
    expect(parsed.services.api).toEqual({ ports: ["5000:3000"] });
    expect(parsed.services.api.environment).toBeUndefined();
  });

  it("emits only `environment:` when a service has env but no ports", () => {
    const input: OverrideInput = {
      config: makeConfig({
        api: { image: "node:20" },
      }),
      allocatedPorts: { compose: {} },
      resolvedEnv: { api: { FOO: "bar" } },
      stackId: "stack-env-only",
    };
    const parsed = yamlParse(generateComposeOverride(input));
    expect(parsed.services.api).toEqual({ environment: { FOO: "bar" } });
    expect(parsed.services.api.ports).toBeUndefined();
  });

  it("output round-trips through yaml.parse without error", () => {
    const out = generateComposeOverride(SINGLE_SERVICE_INPUT);
    // Should not throw; should produce an object.
    const parsed = yamlParse(out);
    expect(typeof parsed).toBe("object");
    expect(parsed).not.toBeNull();
  });

  it("handles the array form of `ports` by indexing on logical-name", () => {
    const input: OverrideInput = {
      config: makeConfig({
        api: {
          ports: [
            { container: 3000, env: "PORT" },
            { container: 9229, env: "DEBUG_PORT" },
          ],
        },
      }),
      // Allocator keys for array-form ports are string indices.
      allocatedPorts: { compose: { api: { "0": 5000, "1": 5001 } } },
      resolvedEnv: { api: {} },
      stackId: "stack-array-ports",
    };
    const parsed = yamlParse(generateComposeOverride(input));
    expect(parsed.services.api.ports).toEqual(["5000:3000", "5001:9229"]);
  });

  it("skips port entries with no container declaration", () => {
    const input: OverrideInput = {
      config: makeConfig({
        api: {
          ports: {
            // Bare-number form: pins host port but doesn't declare container.
            // The override generator skips it (no host:container to emit).
            pinned: 8080,
            http: { container: 3000, env: "PORT" },
          },
        },
      }),
      allocatedPorts: { compose: { api: { pinned: 8080, http: 5000 } } },
      resolvedEnv: { api: {} },
      stackId: "stack-skip-pinned",
    };
    const parsed = yamlParse(generateComposeOverride(input));
    expect(parsed.services.api.ports).toEqual(["5000:3000"]);
  });

  it("accepts a strongly-typed PortDescriptor with `container` (LEV-305 regression)", () => {
    // Before LEV-305, `container` was read structurally via an `unknown`
    // cast inside the override generator because `PortDescriptor` didn't
    // include the field. After LEV-305, the object form of
    // `PortDescriptor` carries `container?: number` as a typed field and
    // the generator consumes it directly. This test pins both halves:
    //  - the type accepts `container` (compile-time)
    //  - the generator still emits the right binding (runtime)
    const httpPort: PortDescriptor = { container: 3000, env: "PORT" };
    const input: OverrideInput = {
      config: makeConfig({
        api: {
          image: "node:20",
          ports: { http: httpPort },
        },
      }),
      allocatedPorts: { compose: { api: { http: 4242 } } },
      resolvedEnv: { api: {} },
      stackId: "stack-typed-port",
    };
    const parsed = yamlParse(generateComposeOverride(input));
    expect(parsed.services.api.ports).toEqual(["4242:3000"]);
  });

  it("skips a typed PortDescriptor with no `container` field", () => {
    // An object-form descriptor without `container` (e.g. an owned-style
    // `{ env }`) gives the override generator nothing to bind. Use case:
    // a port descriptor authored before its container side was decided.
    const noContainer: PortDescriptor = { env: "PORT" };
    const input: OverrideInput = {
      config: makeConfig({
        api: {
          image: "node:20",
          ports: { http: noContainer },
        },
      }),
      allocatedPorts: { compose: { api: { http: 4242 } } },
      resolvedEnv: { api: { FOO: "bar" } },
      stackId: "stack-typed-port-no-container",
    };
    const parsed = yamlParse(generateComposeOverride(input));
    // No ports to bind — the env block is still emitted.
    expect(parsed.services.api.ports).toBeUndefined();
    expect(parsed.services.api.environment).toEqual({ FOO: "bar" });
  });

  it("produces deterministic output regardless of key insertion order", () => {
    const a: OverrideInput = {
      config: makeConfig({
        api: {
          ports: {
            http: { container: 3000, env: "PORT" },
            admin: { container: 3001, env: "ADMIN_PORT" },
          },
        },
      }),
      allocatedPorts: { compose: { api: { http: 5000, admin: 5001 } } },
      resolvedEnv: { api: { B: "2", A: "1" } },
      stackId: "stack-deterministic",
    };
    // Same logical content, different insertion order.
    const b: OverrideInput = {
      config: makeConfig({
        api: {
          ports: {
            admin: { container: 3001, env: "ADMIN_PORT" },
            http: { container: 3000, env: "PORT" },
          },
        },
      }),
      allocatedPorts: { compose: { api: { admin: 5001, http: 5000 } } },
      resolvedEnv: { api: { A: "1", B: "2" } },
      stackId: "stack-deterministic",
    };
    expect(generateComposeOverride(a)).toBe(generateComposeOverride(b));
  });

  it("when no services need overriding, still emits a valid (empty) document", () => {
    const input: OverrideInput = {
      config: makeConfig({ api: { image: "node:20" } }),
      allocatedPorts: { compose: {} },
      resolvedEnv: {},
      stackId: "stack-empty",
    };
    const out = generateComposeOverride(input);
    // Header still present.
    expect(out).toContain("# stack_id: stack-empty");
    // Parses cleanly.
    const parsed = yamlParse(out);
    expect(parsed.services).toEqual({});
  });

  it("emits no `services:` entries for compose services not declared in config", () => {
    // If the allocator hands us ports for a service that doesn't exist
    // in the config, we ignore it — we can't know its container port.
    const input: OverrideInput = {
      config: makeConfig({
        api: { ports: { http: { container: 3000, env: "PORT" } } },
      }),
      allocatedPorts: {
        compose: {
          api: { http: 5000 },
          ghost: { http: 6000 },
        },
      },
      resolvedEnv: { api: {}, ghost: { GHOST: "1" } },
      stackId: "stack-ghost",
    };
    const parsed = yamlParse(generateComposeOverride(input));
    expect(Object.keys(parsed.services)).toEqual(["api"]);
  });
});

// ---------------------------------------------------------------------------
// writeComposeOverride — thin I/O wrapper
// ---------------------------------------------------------------------------

describe("writeComposeOverride", () => {
  let home: string;
  let prevLichHome: string | undefined;

  beforeEach(() => {
    home = mkdtempSync(join(tmpdir(), "lich-override-test-"));
    prevLichHome = process.env.LICH_HOME;
    process.env.LICH_HOME = home;
  });

  afterEach(() => {
    if (prevLichHome === undefined) {
      delete process.env.LICH_HOME;
    } else {
      process.env.LICH_HOME = prevLichHome;
    }
    rmSync(home, { recursive: true, force: true });
  });

  it("writes to ${stackDir}/compose.override.yaml and returns that path", async () => {
    const path = await writeComposeOverride(SINGLE_SERVICE_INPUT);
    const expected = join(
      stackDir(SINGLE_SERVICE_INPUT.stackId),
      "compose.override.yaml",
    );
    expect(path).toBe(expected);

    // File contents match the pure-function output exactly.
    const onDisk = readFileSync(path, "utf8");
    expect(onDisk).toBe(generateComposeOverride(SINGLE_SERVICE_INPUT));
  });

  it("creates the parent directory if it doesn't exist", async () => {
    // We just made a fresh tmpdir; no stack dir exists yet.
    const path = await writeComposeOverride(SINGLE_SERVICE_INPUT);
    const onDisk = readFileSync(path, "utf8");
    expect(onDisk.length).toBeGreaterThan(0);
  });

  it("is idempotent: same input -> identical file content", async () => {
    const path1 = await writeComposeOverride(SINGLE_SERVICE_INPUT);
    const content1 = readFileSync(path1, "utf8");

    const path2 = await writeComposeOverride(SINGLE_SERVICE_INPUT);
    const content2 = readFileSync(path2, "utf8");

    expect(path1).toBe(path2);
    expect(content1).toBe(content2);
  });
});
